        pesan = $("#pesan").val(),
        sendData = "pengirim=" + pengirim + "&email=" + email + "&pesan=" + pesan;

      $.ajax({
        headers: {
          'X-CSRF-TOKEN': "{{ csrf_token() }}"
        },
        url: "{{ route('send') }}",
        type: "POST",
        data: sendData,
        dataType: 'json',
        beforeSend: function () {
          loadingBeforeSend();
        },
        success: function (data) {
          loadingAfterSend();
          $("#pengirim").val("");
          $("#email").val("");
          $("#pesan").val("");

          toastr.options = {
            "closeButton": false,
            "debug": false,
            "newestOnTop": false,
            "progressBar": true,
            "positionClass": "toast-top-right",
            "preventDuplicates": false,
            "onclick": null,
            "showDuration": "300",
            "hideDuration": "1000",
            "timeOut": "3000",
            "extendedTimeOut": "1000",
            "showEasing": "swing",
            "hideEasing": "linear",
            "showMethod": "fadeIn",
            "hideMethod": "fadeOut"
          };
          toastr.success(data.msg);
        },
        error: function (resp) {
          loadingAfterSend();
          if (_.has(resp.responseJSON, 'errors')) {
            _.map(resp.responseJSON.errors, function (val, key) {
              $('#' + key + '-error').html(val[0]).fadeIn(1000).fadeOut(5000);
            })
          }
          alert(resp.responseJSON.message)
        }
      });
    });
  });

  $('#send').hover(function () {
    $('.title-button').css('color', '#fff');
  });

  function loadingBeforeSend() {
    $("#send").attr('disabled', 'disabled');
    $("#send").text('Kirim Pesan....');
  }

  function loadingAfterSend() {
    $("#send").removeAttr('disabled');
    $("#send").text('Send Message');
  }
</script>
